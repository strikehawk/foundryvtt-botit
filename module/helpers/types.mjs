export class GenericItem {
    /**
     * A unique identifier of the item within its parent collection.
     * 
     * @type {string}
     * @memberof GenericItem
     */
    key;

    /**
     * The user-friendly label of the item.
     * 
     * @type {string}
     * @memberof GenericItem
     */
    label;

    /**
     * A user-friendly description of the item.
     * 
     * @type {string}
     * @memberof GenericItem
     */
    description;
}